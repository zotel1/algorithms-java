package matrices;

public class EjemploDos {

    public static void main(String[] args) {

        // initial array
        int[] arr = { 1, 2, 3, 4, 5 };

        // size of array
        int n = arr.length;

        // traversing array
        for (int i = 0; i < n; i++)
            System.out.println(arr[i] + " ");
    }
}
